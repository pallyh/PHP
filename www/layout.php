
    <h1>Передача параметров. Шаблоизация</h1>
    <h2>.htaccess и диспетчер доступа</h2>
    <p>
        По требованию безопасности (сертифицированным) ПО должно иметь
        диспетчер доступа - точку прохождения всех запросов.
        PHP   усложнет это требование, т.к. формально каждый файл является
        "точкой входа".

    </p>
    <p>
        Одно из решений использовать модуль обработки запросо
        apache. Его инструкции задаются в специальном файле - .htaccess
    </p>
